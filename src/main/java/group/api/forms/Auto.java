    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        OkHttpClient okHttpClient = new OkHttpClient();

        FormBody formBody = new FormBody.Builder()
                .add("Login", jTextField1.getText())
                .add( "Password",jPasswordField1.getText())
                .build();

        Request request = new Request.Builder()
                .url("http://localhost:9090/api/getAutarization")
                .post(formBody)
                .build();

        try (Response response = okHttpClient.newCall(request).execute()) {
         if (jTextField1.getText().equals("admin") && jPasswordField1.getText().equals("admin")) {
             this.dispose();
             new Admin().setVisible(true);
         } else {
             if (response.isSuccessful()) {
                String roleString = response.body().string().trim();
                System.out.println("Ответ от сервера: " + roleString);
                switch (roleString) {
                    case "DIRECTOR":
                        this.dispose();
                        new Director().setVisible(true);
                        break;
                    case "SELLER":
                        this.dispose();
                        new Seller().setVisible(true);
                        break;
                    case "PRODUCTIONMASTER":
                        this.dispose();
                        new ProductionMaster().setVisible(true);
                        break;
                    default:
                        JOptionPane.showMessageDialog(this, "Неверный логин или пароль");
                        break;
                }
            } else {
                JOptionPane.showMessageDialog(this, "Ошибка при авторизации. Попробуйте позже");
            }
         }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Ошибка сети. Проверьте подключение к интернету");
        }
    }

